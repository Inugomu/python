#Cuatro operaciones basicas.
numero1 = float(input("Ingrese el número 1:  "))
numero2 = float(input("Ingrese el número 2:  "))
numero3 = float(input("Ingrese el número 3:  "))
numero4 = float(input("Ingrese el número 4:  "))

suma = numero1 + 5
resta = numero2 - 6
multiplicacion = numero3 * 7
division = numero4 / 8

print(numero1, "+ 5 =", suma)
print(numero2, "- 5 =", resta)
print(numero3, "* 7 =", multiplicacion)
print(numero4, "/ 8 =", division)
